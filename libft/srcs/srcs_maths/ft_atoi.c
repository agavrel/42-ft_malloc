#include "libft.h"

int			ft_atoi(char *str)
{
	int		s;
	long	r;
	int		i;

	i = 0;
	r = 0;
	s = (str[i] == '-') ? -1 : 1;
	(str[i] == '-' || str[i] == '+') ? i++ : i;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
		r = r * 10 + str[i++] - '0';
	return (s * (int)r);
}
